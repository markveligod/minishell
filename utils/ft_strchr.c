#include "../minish.h"

int ft_strchr(const char *s)
{
	int i;
	char c;

	i = 0;
	if (!s)
		return (0);
	while (s[i])
	{
		if (s[i] == '\"')
		{
			i++;
			while (s[i] && s[i] != '\"')
				i++;
			i++;
		}
		if (s[i] == '\'')
		{
			i++;
			while (s[i] && s[i] != '\'')
				i++;
			i++;
		}
		if (s[i] == '|' || s[i] == ';' || s[i] == '\n')
			return (i);
		i++;
	}
	return (i);
}
